package Classes_;

public class Animal {
	
	String cor;
	int idade;
	int peso ;
	String nome;
	

	public String getCor() {
		return cor;		
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade= idade;
	}
	public int getPeso() {
		return peso;	
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	public String getNome() {
		return nome;		
	}
	public void setNome(String nome) {
		this.nome = nome;

}
}
